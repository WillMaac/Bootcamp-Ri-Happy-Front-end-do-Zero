//Objeto é uma coleção dinâmica de chave e valor//

//Exemplo//
const pessoa ={nome: 'Willian',
    idade: 20
}

//Como acessar o valor//
console.log(pessoa.nome);
console.log(pessoa.idade);
console.log(pessoa);

//Como adicionar um valor em Objeto//
pessoa.altura = 1.70;
console.log(pessoa);

//Como deletar um valor em Objeto//
delete pessoa.idade
console.log(pessoa);


