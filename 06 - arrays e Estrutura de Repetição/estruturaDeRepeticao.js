/*Estruturas de repetição, também conhecidas como loops, são mecanismos fundamentais em todas linguagens de programação pois  permitem executar um bloco de código repetidamente enquanto uma determinada condição for verdadeira. Elas são essenciais para automatizar tarefas, percorrer listas de dados e realizar cálculos iterativos.
*/

